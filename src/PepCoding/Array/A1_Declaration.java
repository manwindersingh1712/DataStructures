package PepCoding.Array;

public class A1_Declaration {
    public static void main(String[] args) {
        int[] marks = new int[5];

        marks[0] = 33;
        marks[1] = 12;
        marks[2] = 70;
        marks[3] = 90;
        marks[4] = 47;

        for (int i = 0; i < marks.length; i++) {
            System.out.println(marks[i]);
        }

    }
}
